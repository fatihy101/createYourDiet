package dietCreator;

public class food {
    boolean isLight;
    String name;
    int caloriesOfFood;
    String type;
    public food(String name, int CaloriesOfFood, boolean isLight,String type)
    {
        this.caloriesOfFood = CaloriesOfFood;
        this.name.equals(name);
        this.isLight = isLight;
        this.type.equals(type);
    }
}
